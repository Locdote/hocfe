package demo.pheptoan;

public class PhepToan {

	public static void main(String[] args) {
		// Phep cong
		int phepCong = 2 + 3;
		int so1 = 4, so2 = 5;
		int tongHaiSo = so1 + so2;
		System.out.println("phepCong: " + phepCong);
		System.out.println("tongHaiSo: " + tongHaiSo);
		
		// Phep tru
		int phepTru = 10 - 5;
		// Phep nhan
		int phepNhan = 5 * 5;
		// Phep chia
		double phepChia = (double)100 / (double)9;
		System.out.println("phepTru: " + phepTru);
		System.out.println("phepNhan: " + phepNhan);
		System.out.println("phepChia: " + phepChia);
	}
}
