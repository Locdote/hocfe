package demo.dieukien;

public class CauDieuKien {

	public static void main(String[] args) {
		int dieuKien = 10;
		
		// Vi du ve nhieu dieu kien
		if (dieuKien == 0) {
			System.out.println("dieu kien bang 0");
		} else if (dieuKien > 0) {
			System.out.println("dieu kien lon hon 0");
		} else {
			System.out.println("dieu kien nho hon 0");
		}
		
		// Vi du mot dieu kien
		if (dieuKien == 10) {
			System.out.println("dieu kien bang 10");
		}
		
		// if else
		if (dieuKien == 10) {
			System.out.println("dieu kien bang 10");
		} else {
			System.out.println("dien kien khong bang 10");
		}
		
	}

}
